#making input and action after input with conditional
grade = input("Please input your your grade ")

#make a condition according to input
if(grade == "A"):
    print("You are awesome")
elif(grade == "B"):
    print("Your are impresive")
elif(grade == "C"):
    print("You have warning")
elif(grade == "D"):
    print("You have red alert")
elif(grade == "E"):
    print("Please go back next semester")
else:
    print("Unknown Grade")
